"""
Script que contiene funciones de suma

"""
import random

def suma(valor_1:(int, float),valor_2:(int, float)):
    """Devuelve la suma de 2 valores

    Args:
        valor_1 (int, float): ingresar primer valor
        valor_2 (int, float): ingresar segundo valor

    Returns:
        (int, float): devuelve la suma de dos valores
    """
    return valor_1 + valor_2

